class Solution(object):
    def totalNQueens(self, n):
        """
        :type n: int
        :rtype: int
        """

        def back(row):
            if row == n:
                return 1
            else:
                count = 0
                for i in range(n):
                    if i in columns or row - i in d1 or row + i in d2:
                        continue
                    columns.add(i)
                    d1.add(row - i)
                    d2.add(row + i)
                    count += back(row + 1)
                    columns.remove(i)
                    d1.remove(row - i)
                    d2.remove(row + i)
                return count

        columns = set()
        d1 = set()
        d2 = set()
        return back(0)

