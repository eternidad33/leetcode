# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def preorderTraversal(self, root: TreeNode) -> List[int]:
        def before(root, res):
            if not root:
                return None
            res.append(root.val)
            before(root.left, res)
            before(root.right, res)

        res = []
        before(root, res)
        return res

