class Solution {
    public int maxDepth(TreeNode root) {
        //æ–¹æ³•1ï¼šå±‚æ¬¡éå†
        if (root == null) {
            return 0;
        }
        List<TreeNode> queue = new LinkedList<>();
        queue.add(root);
        List<TreeNode> tmp;
        int res = 0;
        while (!queue.isEmpty()) {
            //å­˜å‚¨æ¯å±‚çš„ä¸‹ä¸€å±‚èŠ‚ç‚¹
            tmp = new LinkedList<>();
            for (TreeNode node : queue) {
                if (node.left != null) {
                    tmp.add(node.left);
                }
                if (node.right != null) {
                    tmp.add(node.right);
                }
            }
            queue = tmp;
            res++;
        }
        return res;
    }
}
